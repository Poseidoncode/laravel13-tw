console.log('=== 開始修復翻譯 ===\n');

const files = fs.readdirSync(translatedDir).filter(f => f.endsWith('.astro')).sort();

let fixedCount = 0;

for (const file of files) {
  const filePath = path.join(translatedDir, file);
  let content = fs.readFileSync(filePath, 'utf8');
  
  let modified = false;
  
  // Replace H2 titles
  for (const [english, chinese] of Object.entries(translationMap)) {
    // Match H2 with exact English text
    const regex = new RegExp(`<h2[^>]*>\\s*${english}\\s*</h2>`, 'gi');
    if (regex.test(content)) {
      content = content.replace(regex, `<h2>\${chinese}</h2>`);
      modified = true;
      console.log(`✅ ${file}: "${english}" → "${chinese}"`);
    }
  }
  
  if (modified) {
    fs.writeFileSync(filePath, content, 'utf8');
    fixedCount++;
  }
}

console.log(`\n✅ 修復完成，共修復 ${fixedCount} 個文件`);
